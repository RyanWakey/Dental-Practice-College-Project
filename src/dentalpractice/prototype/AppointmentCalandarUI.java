    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        GregorianCalendar gregCal = new GregorianCalendar();
        realDay = gregCal.get(GregorianCalendar.DAY_OF_MONTH);
        realMonth = gregCal.get(GregorianCalendar.MONTH);
        realYear = gregCal.get(GregorianCalendar.YEAR);
        currentMonth = realMonth;
        currentYear = realYear;
        
        System.out.println("realYear="+realYear);
        System.out.println("realMonth="+realMonth);
        
        
        DefaultTableModel model=(DefaultTableModel)CalTbl.getModel();
        String[] headers = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"}; //All headers

        for (int i=0; i<7; i++)
        {
            model.addColumn(headers[i]);
        }
        
        CalTbl.setColumnSelectionAllowed(true);
        CalTbl.setRowSelectionAllowed(true);
        CalTbl.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        CalTbl.setRowHeight(38);
        model.setRowCount(6);
        model.setColumnCount(7);
        
        for (int i=realYear-100; i<=realYear+100; i++)
        {
            yearCmb.addItem(String.valueOf(i));
        }

        refreshCal(realMonth, realYear);
        
         
    }//GEN-LAST:event_formWindowOpened

