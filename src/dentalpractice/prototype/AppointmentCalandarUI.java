    private void CalTblcalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CalTblcalMouseClicked
        int row=CalTbl.rowAtPoint(evt.getPoint());          // Gets the row where the mouse is pointing
        int col= CalTbl.columnAtPoint(evt.getPoint());      // Gets the col where the mouse is pointing

        try
        {
            selectedDate=Integer.parseInt(CalTbl.getValueAt(row, col).toString());
        }
        catch(Exception e)
        {
            selectedDate=0;
            //e.printStackTrace();
        }
        System.out.println("Date: "+selectedDate);
        selectedMonth=getMonth(MonthLbl.getText());
        System.out.println("Month: "+selectedMonth);
        try
        {
            selectedYear=Integer.parseInt(yearCmb.getSelectedItem().toString());
        }
        catch(Exception ex)
        {
            selectedYear=realYear;
        }
        System.out.println("Year: "+selectedYear);
        if(selectedDate>=1 && selectedDate<=31 && selectedMonth>=1 && selectedMonth<=12)
        {
            CalendarOptionsUI CalOpt=new CalendarOptionsUI();
            CalOpt.setVisible(true);
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Invalid Date");
        }

    }//GEN-LAST:event_CalTblcalMouseClicked
public void refreshCal(int month, int year)
        {
            String[] months =  {"January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"};
            
            int numOfDays;
            int startOfMonth;
            
            MonthLbl.setText(months[month]);
            yearCmb.setSelectedItem(String.valueOf(year));
            
            btnNextMonth.setEnabled(true);
            btnNextYear.setEnabled(true);
            btnPrevMonth.setEnabled(true);
            btnPrevYear.setEnabled(true);
            if(month==11)
            {
                btnNextMonth.setEnabled(false);
            }
            if(month==0)
            {
                btnPrevMonth.setEnabled(false);
            }
            if(year==realYear-10)
            {
                btnPrevYear.setEnabled(false);
            }
            if(year==realYear+100)
            {
                btnNextYear.setEnabled(false);
            }
            
            //Clearing the previous values in calendar
            for(int i=0;i<6;i++)
            {
                for(int j=0;j<7;j++)
                {
                    CalTbl.setValueAt(null, i, j);    //Setting null value to every cell 
                }
            }
            
            GregorianCalendar calendar = new GregorianCalendar(year, month, 1); //Gives first day of the month
            numOfDays = calendar.getActualMaximum(GregorianCalendar.DAY_OF_MONTH); //Gives total Number of days
            startOfMonth=calendar.get(GregorianCalendar.DAY_OF_WEEK); // Gives the starting day
            System.out.println("startOfMonth = "+startOfMonth);
            System.out.println("numOfDays = "+numOfDays);
            
            
            // Making the new calendar
            
            for(int i=1;i<=numOfDays;i++)
            {
                int row = (i+startOfMonth-2)/7;
                
                int column  =  ((i+startOfMonth)%7-2%7 + 7)%7;                                                  
                
                CalTbl.setValueAt(i, row, column);
                
                CalTbl.setDefaultRenderer(CalTbl.getColumnClass(0), new TableCellRenderer());
            }
        }
 public int getMonth(String month){
            int monthNumber=-1;
            if(month=="January")
            {
                monthNumber=1;
            }
            else if(month=="February")
            {
                monthNumber=2;
            }
            else if(month=="March")
            {
                monthNumber=3;
            }
            else if(month=="April")
            {
                monthNumber=4;
            }
            else if(month=="May")
            {
                monthNumber=5;
            }
            else if(month=="June")
            {
                monthNumber=6;
            }
            else if(month=="July")
            {
                monthNumber=7;
            }
            else if(month=="August")
            {
                monthNumber=8;
            }
            else if(month=="September")
            {
                monthNumber=9;
            }
            else if(month=="October")
            {
                monthNumber=10;
            }
            else if(month=="November")
            {
                monthNumber=11;
            }
            else if(month=="December")
            {
                monthNumber=12;
            }
            return monthNumber;
        }
          
        
